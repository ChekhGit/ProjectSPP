package com.spp.chekh.pmfrontend.view.model.custom;

public class CoachTableViewModel {
    private String id;
    private String name;
    private String surname;
    private String yearsOld;
    private String idStatistic;
    private String titles;
    private String winMatches;
    private String lostMatches;
    private String drawMatches;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getSurname() {
        return surname;
    }

    public void setSurname(String surname) {
        this.surname = surname;
    }

    public String getYearsOld() {
        return yearsOld;
    }

    public void setYearsOld(String yearsOld) {
        this.yearsOld = yearsOld;
    }

    public String getIdStatistic() {
        return idStatistic;
    }

    public void setIdStatistic(String idStatistic) {
        this.idStatistic = idStatistic;
    }

    public String getTitles() {
        return titles;
    }

    public void setTitles(String titles) {
        this.titles = titles;
    }

    public String getWinMatches() {
        return winMatches;
    }

    public void setWinMatches(String winMatches) {
        this.winMatches = winMatches;
    }

    public String getLostMatches() {
        return lostMatches;
    }

    public void setLostMatches(String lostMatches) {
        this.lostMatches = lostMatches;
    }

    public String getDrawMatches() {
        return drawMatches;
    }

    public void setDrawMatches(String drawMatches) {
        this.drawMatches = drawMatches;
    }
}
